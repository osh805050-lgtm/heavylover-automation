def build_sheet(ws, data_rows):
    for col_idx, h in enumerate(headers, 1):
        c = ws.cell(row=1, column=col_idx, value=h)
        c.fill = header_fill
        c.font = header_font
        c.alignment = center_align
        c.border = thin_border

    for r_idx, row in enumerate(data_rows, start=2):
        category = row[1] if row[1] else row[0]
        fill_color = COLORS.get(category, "FFFFFF")
        row_fill = PatternFill("solid", fgColor=fill_color)
        is_urgent = (category == "즉시검토") or ("🔥" in str(row[5]))

        for c_idx, val in enumerate(row, 1):
            c = ws.cell(row=r_idx, column=c_idx, value=val)
            c.fill = row_fill
            c.font = urgent_font if is_urgent else data_font
            if c_idx in (3, 5, 6, 11):  # 번호·적합도·상태·합격률 가운데
                c.alignment = center_align
            else:
                c.alignment = left_align
            c.border = thin_border

    for col, w in widths.items():
        ws.column_dimensions[col].width = w

    ws.row_dimensions[1].height = 34
    for r in range(2, len(data_rows) + 2):
        ws.row_dimensions[r].height = 80  # 합격노하우 길어서 높이 늘림

    ws.freeze_panes = "A2"
    ws.auto_filter.ref = ws.dimensions


